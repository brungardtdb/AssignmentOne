public class PrintName
{
// Constructor for PrintName class
  public PrintName(String name, String movieTitle)
  {
    // Print introduction
    System.out.println("My name is " + name);
    // Print favorite movie title
    System.out.println("My favorite movie is " + movieTitle);
  }// End of consructor
}// End of PrintName class
